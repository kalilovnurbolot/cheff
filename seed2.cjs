async function seed() {
  console.log(`Начинаем добавление ${recipes.length} рецептов...\n`)

  let ok = 0, fail = 0
  for (const recipe of recipes) {
    const { data, error } = await supabase.from('recipes').insert(recipe).select('id, title').single()
    if (error) {
      console.error(`✗ ${recipe.title}: ${error.message}`)
      fail++
    } else {
      console.log(`✓ ${data.title}`)
      ok++
    }
  }

  console.log(`\nГотово: ${ok} добавлено, ${fail} ошибок.`)
}

