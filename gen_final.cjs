const { Document, Packer, Paragraph, TextRun, AlignmentType, UnderlineType,
        PageBreak, Footer, PageNumber, LineRuleType, Table, TableRow, TableCell,
        WidthType, BorderStyle, TabStopType } = require("docx");
const fs = require("fs");

// ─── Форматтоо константтары ───────────────────────────────────────────────────
const FONT = "Times New Roman";
const SZ   = 28;          // 14pt  = 28 half-points
const SZ24 = 24;          // 12pt
const SZ22 = 22;          // 11pt (код)
const SZ20 = 20;          // 10pt

const PAGE = {
  size:   { width: 11906, height: 16838 },
  margin: { top: 1134, right: 567, bottom: 1134, left: 1701 }
};
const SP = { line: 360, lineRule: LineRuleType.AUTO };

// ─── Жардамчы функциялар ──────────────────────────────────────────────────────

const run = (text, opts = {}) =>
  new TextRun({ text, font: FONT, size: SZ, ...opts });

const footer = () =>
  new Footer({ children: [
    new Paragraph({ alignment: AlignmentType.RIGHT,
      children: [new TextRun({ children: [PageNumber.CURRENT], font: FONT, size: SZ })] })
  ]});

/** Бөлүм аталышы — жаңы баракта, борборлоштурулган */
const chTitle = (text) =>
  new Paragraph({ pageBreakBefore: true, alignment: AlignmentType.CENTER,
    spacing: { before: 0, after: 480, ...SP },
    children: [run(text, { bold: true })] });

/** Параграф аталышы 2-деңгээл */
const h2 = (text) =>
  new Paragraph({ spacing: { before: 360, after: 240, ...SP },
    children: [run(text, { bold: true })] });

/** Параграф аталышы 3-деңгээл */
const h3 = (text) =>
  new Paragraph({ spacing: { before: 280, after: 160, ...SP },
    children: [run(text, { bold: true, underline: { type: UnderlineType.SINGLE } })] });

/** Негизги текст */
const p = (text) =>
  new Paragraph({ alignment: AlignmentType.BOTH, spacing: { after: 0, ...SP },
    indent: { firstLine: 709 }, children: [run(text)] });

/** Тизме пункту */
const li = (text) =>
  new Paragraph({ alignment: AlignmentType.BOTH, spacing: { after: 80, ...SP },
    indent: { left: 709 }, children: [run(`— ${text}`)] });

/** Номурланган тизме */
const ni = (n, text) =>
  new Paragraph({ alignment: AlignmentType.BOTH, spacing: { after: 100, ...SP },
    indent: { left: 709, hanging: 355 }, children: [run(`${n}. ${text}`)] });

/** Код сабы */
const code = (text) =>
  new Paragraph({ spacing: { after: 40, line: 260, lineRule: LineRuleType.AUTO },
    indent: { left: 709 },
    children: [new TextRun({ text, font: "Courier New", size: SZ22 })] });

/** Бош сап */
const gap = () =>
  new Paragraph({ spacing: { after: 0, ...SP }, children: [] });

/** Таблица подписи */
const label = (text) =>
  new Paragraph({ alignment: AlignmentType.CENTER,
    spacing: { before: 160, after: 120, ...SP },
    children: [run(text, { bold: true })] });

/** Таблица эскертмеси */
const tNote = (text) =>
  new Paragraph({ spacing: { before: 80, after: 200, line: 320, lineRule: LineRuleType.AUTO },
    children: [run(`Эскертүү: ${text}`, { italic: true, size: SZ24 })] });

// ─── Таблица жардамчылары ─────────────────────────────────────────────────────
const B = { style: BorderStyle.SINGLE, size: 4, color: "000000" };
const brd = { top: B, bottom: B, left: B, right: B };

const tc = (text, bold = false, shade = false) =>
  new TableCell({ borders: brd, shading: shade ? { fill: "D9D9D9" } : undefined,
    margins: { top: 60, bottom: 60, left: 100, right: 100 },
    children: [new Paragraph({ alignment: AlignmentType.CENTER,
      spacing: { after: 0, line: 280, lineRule: LineRuleType.AUTO },
      children: [new TextRun({ text, bold, font: FONT, size: SZ24 })] })] });

const tcL = (text, bold = false) =>
  new TableCell({ borders: brd,
    margins: { top: 60, bottom: 60, left: 100, right: 100 },
    children: [new Paragraph({ alignment: AlignmentType.BOTH,
      spacing: { after: 0, line: 280, lineRule: LineRuleType.AUTO },
      children: [new TextRun({ text, bold, font: FONT, size: SZ24 })] })] });

const tr = (cells) => new TableRow({ children: cells });

const tbl = (rows) =>
  new Table({ width: { size: 100, type: WidthType.PERCENTAGE }, rows });

